    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked

        int fila=tabla.getSelectedRow();

        if(fila==-1){
            JOptionPane.showMessageDialog(null, "Error al seleccionar");
        }else{
            String nom= (String) tabla.getValueAt(fila,0);
            String apellido= (String) tabla.getValueAt(fila,1);
            String correo= (String) tabla.getValueAt(fila,2);
            String tel= (String) tabla.getValueAt(fila,3);

            nomtext.setText(nom);
            apellidotext.setText(apellido);
            emailtext.setText(correo);
            celtext.setText(tel);
        }

    }//GEN-LAST:event_tablaMouseClicked

