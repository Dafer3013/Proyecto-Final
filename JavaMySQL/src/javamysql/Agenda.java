    void limpiar(){
        
        for(int x=0; x<tabla.getRowCount(); x++){
            modelo.removeRow(x);
            x=x-1;
        }
    }
    
    void consultar(){
        filtro=alumno.getAlumno();
        
        alumnotext.setText(filtro);
        
        String SQL="select * from contact_list where alumno='"+filtro+"'";
        try{
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery(SQL);
        
        Object [] contactos= new Object [4];
        modelo=(DefaultTableModel) tabla.getModel();
        
        while(rs.next()){
            contactos[0]=rs.getString("nombre");
            contactos[1]=rs.getString("apellido");
            contactos[2]=rs.getString("email");
            contactos[3]=rs.getString("telefono");
            
            modelo.addRow(contactos);
        }
        tabla.setModel(modelo);
                
        } catch (Exception e){
            
        }
    }

